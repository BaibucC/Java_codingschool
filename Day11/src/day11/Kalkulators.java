    private void setElementText(String value) {
        if (finalRes) {
            if (value.equals("0")) {
                calc.setText("");
            }
            calc.setText("");
            finalRes = false;
        }
        if (actionChosen) {
            calc.setText("");
            actionChosen = false;
        }
        if (!finalRes) {
            if (value.equals("0") && calc.getText().equals("")) {
                calc.setText("");
            } else {
                value = calc.getText() + value;
                calc.setText(value);
            }
        }
    }

