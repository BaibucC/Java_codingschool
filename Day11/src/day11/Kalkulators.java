        if (!finalRes) {
            switch (action) {
                case "sum":
                    result = sum.Sum(number1, number2);
                    break;
                case "minus":
                    result = sum.Minus(number1, number2);
                    break;
                case "multi":
                    result = sum.Multi(number1, number2);
                    break;
                case "div":
                    result = sum.Div(number1, number2);
                    break;
            }
            calc.setText(String.valueOf(result));
            finalRes = true;
        }
