    private void loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginActionPerformed
        String inputUsername = username.getText();
        String inputPassword = password.getText();

        if (inputUsername.length() > 7 && inputPassword.length() > 9) {
            if (inputPassword.contains("123")) {
                result.setText("Password illegal, can't contain 123!");
            } else {
                if (inputUsername.equals(username1) && inputPassword.equals(password1)) {
                    result.setText("Login ok!");
                } else {
                    result.setText("Wrong login!");
                }
            }
        } else {
            result.setText("Enter login");
        }

    }//GEN-LAST:event_loginActionPerformed

